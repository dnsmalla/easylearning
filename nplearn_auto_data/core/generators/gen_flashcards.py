def create_flashcard(level_prefix, word, meaning, roman, desc, level, category, idx):
    return {
        "id": f"{level_prefix}_{category}_{idx:03d}",
        "front": word,
        "back": meaning,
        "romanization": roman,
        "meaning": desc,
        "level": level,
        "category": category,
        "examples": [word],
        "isFavorite": False,
        "reviewCount": 0,
        "correctCount": 0
    }

def generate_level_flashcards(level, vocab_dict, prefix):
    flashcards = []
    idx = 1
    for category, words in vocab_dict.items():
        for word, meaning, roman, desc in words:
            flashcards.append(create_flashcard(prefix, word, meaning, roman, desc, level, category, idx))
            idx += 1
    return flashcards

